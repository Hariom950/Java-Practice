public class HalfRact {
    public static void main(String[] args) {
        int n=4;
        for(int i=1; i<=n; i++) {
            for(int j=1; j<=i; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
      
    }   //-->for inverted pattern we use ->for(i=n;i>=1;i--)
        // this is outer loop
}
